    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        int x=dbo.checkLogin("agentdetails",txtUsername.getText(), NewUser.encrypt(txtPassword.getText()));
        
        AgentSelectionWindow asWin=new AgentSelectionWindow();
        if(x==0){
            loggedAs=txtUsername.getText();
            asWin.setTxtLogAs(loggedAs);
            System.out.println(loggedAs);
            //asWin.txtLogAs.setText(LoginFrame.loggedAs);
            //asWin.txtLogAs.setText(LoginFrame.loggedAs);
            this.dispose();
            asWin.pack();
            asWin.setLocationRelativeTo(null);
            asWin.setVisible(true);
            
        }
        else if(x==1){
            JOptionPane.showMessageDialog(this, "Invalid username or password");
            txtPassword.setText("");
        }
        else{
            JOptionPane.showMessageDialog(this, "Error occured while checking username or password");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

